class Solution
{
public:
    vector<int> subsetSums(vector<int> arr, int N)
    {
        vector<int> ans;
        // Write Your Code here
        int n=1<<N;
        for(int j=0;j<n;j++){
            int val=0;
            for(int i=0;i<N;i++){
                if(j&(1<<i)){
                    val+=arr[i];
                    // cout<<arr[j];
                }
            }
            ans.push_back(val);
        }
        return ans;
    }
};

